/*
 * local_store — implementation: encrypted local state at rest (Zero Knowledge).
 *
 * Self-describing AEAD containers (AES-256-GCM / ChaCha20-Poly1305) with the
 * 36-byte header authenticated as AAD, and an Argon2id KDF for passphrase-based
 * keys. Built on OpenSSL 3.x native primitives. Fail-closed: a failed tag check
 * releases no plaintext (the unverified buffer is wiped before return).
 *
 * No filesystem/network I/O; only the OS RNG is read for salt/nonce.
 */

#include "local_store.h"

#include <stdlib.h>
#include <string.h>

#include <openssl/core_names.h>
#include <openssl/crypto.h>
#include <openssl/evp.h>
#include <openssl/kdf.h>
#include <openssl/params.h>
#include <openssl/rand.h>

/* OpenSSL < 3.3 compat: older core_names.h lacks these Argon2 param names
 * (this is what install.sh's sed patch injected; kept here once, properly). */
#ifndef OSSL_KDF_PARAM_ARGON2_LANES
#define OSSL_KDF_PARAM_ARGON2_LANES "lanes"
#endif
#ifndef OSSL_KDF_PARAM_ARGON2_MEMCOST
#define OSSL_KDF_PARAM_ARGON2_MEMCOST "memcost"
#endif
#ifndef OSSL_KDF_PARAM_THREADS
#define OSSL_KDF_PARAM_THREADS "threads"
#endif

#define LS_VERSION       1u
#define LS_KDF_NONE      0u
#define LS_KDF_ARGON2ID  1u

/* Argon2id v1 parameters (OWASP interactive minimums). */
#define LS_ARGON2_T      2u      /* passes (t_cost) */
#define LS_ARGON2_M_KIB  19456u  /* memory cost in KiB (19 MiB) */
#define LS_ARGON2_P      1u      /* lanes / parallelism */

/* Container field offsets. */
#define OFF_MAGIC   0u
#define OFF_VERSION 4u
#define OFF_AEAD    5u
#define OFF_KDF     6u
#define OFF_SALT    8u
#define OFF_NONCE   24u

static const uint8_t LS_MAGIC[4] = { 'F', 'R', 'Z', 'S' };

/* --- AEAD selection --- */

static const EVP_CIPHER *cipher_for(ls_aead aead) {
    switch (aead) {
        case LS_AEAD_AES256_GCM:        return EVP_aes_256_gcm();
        case LS_AEAD_CHACHA20_POLY1305: return EVP_chacha20_poly1305();
        default:                        return NULL;
    }
}

/* --- KDF: Argon2id --- */

static ls_status argon2id_derive(const uint8_t *pass, size_t pass_len,
                                 const uint8_t *salt, size_t salt_len,
                                 uint8_t *out, size_t out_len) {
    EVP_KDF *kdf = EVP_KDF_fetch(NULL, "ARGON2ID", NULL);
    if (kdf == NULL) return LS_ERR_KDF;
    EVP_KDF_CTX *kctx = EVP_KDF_CTX_new(kdf);
    EVP_KDF_free(kdf);
    if (kctx == NULL) return LS_ERR_KDF;

    uint32_t iter = LS_ARGON2_T, lanes = LS_ARGON2_P;
    uint32_t memcost = LS_ARGON2_M_KIB, threads = LS_ARGON2_P;

    OSSL_PARAM params[7];
    size_t n = 0;
    params[n++] = OSSL_PARAM_construct_octet_string(OSSL_KDF_PARAM_PASSWORD,
                                                    (void *)pass, pass_len);
    params[n++] = OSSL_PARAM_construct_octet_string(OSSL_KDF_PARAM_SALT,
                                                    (void *)salt, salt_len);
    params[n++] = OSSL_PARAM_construct_uint32(OSSL_KDF_PARAM_ITER, &iter);
    params[n++] = OSSL_PARAM_construct_uint32(OSSL_KDF_PARAM_ARGON2_LANES, &lanes);
    params[n++] = OSSL_PARAM_construct_uint32(OSSL_KDF_PARAM_ARGON2_MEMCOST, &memcost);
    params[n++] = OSSL_PARAM_construct_uint32(OSSL_KDF_PARAM_THREADS, &threads);
    params[n]   = OSSL_PARAM_construct_end();

    int rc = EVP_KDF_derive(kctx, out, out_len, params);
    EVP_KDF_CTX_free(kctx);
    return (rc == 1) ? LS_OK : LS_ERR_KDF;
}

ls_status ls_derive_key(const uint8_t *passphrase, size_t pass_len,
                        const uint8_t *salt, size_t salt_len,
                        uint8_t out_key[LS_KEY_LEN]) {
    if (passphrase == NULL || salt == NULL || out_key == NULL) return LS_ERR_NULL_ARG;
    return argon2id_derive(passphrase, pass_len, salt, salt_len, out_key, LS_KEY_LEN);
}

/* --- AEAD primitives --- */

static ls_status aead_encrypt(const EVP_CIPHER *cipher, const uint8_t *key,
                              const uint8_t *nonce,
                              const uint8_t *aad, size_t aad_len,
                              const uint8_t *pt, size_t pt_len,
                              uint8_t *ct, uint8_t *tag) {
    EVP_CIPHER_CTX *ctx = EVP_CIPHER_CTX_new();
    if (ctx == NULL) return LS_ERR_OOM;

    ls_status st = LS_ERR_CRYPTO;
    int len = 0, total = 0;
    if (EVP_EncryptInit_ex(ctx, cipher, NULL, NULL, NULL) != 1) goto done;
    if (EVP_CIPHER_CTX_ctrl(ctx, EVP_CTRL_AEAD_SET_IVLEN, LS_NONCE_LEN, NULL) != 1) goto done;
    if (EVP_EncryptInit_ex(ctx, NULL, NULL, key, nonce) != 1) goto done;
    if (aad_len > 0 && EVP_EncryptUpdate(ctx, NULL, &len, aad, (int)aad_len) != 1) goto done;
    if (pt_len > 0) {
        if (EVP_EncryptUpdate(ctx, ct, &len, pt, (int)pt_len) != 1) goto done;
        total = len;
    }
    if (EVP_EncryptFinal_ex(ctx, ct + total, &len) != 1) goto done;
    if (EVP_CIPHER_CTX_ctrl(ctx, EVP_CTRL_AEAD_GET_TAG, LS_TAG_LEN, tag) != 1) goto done;
    st = LS_OK;
done:
    EVP_CIPHER_CTX_free(ctx);
    return st;
}

static ls_status aead_decrypt(const EVP_CIPHER *cipher, const uint8_t *key,
                              const uint8_t *nonce,
                              const uint8_t *aad, size_t aad_len,
                              const uint8_t *ct, size_t ct_len,
                              const uint8_t *tag, uint8_t *pt) {
    EVP_CIPHER_CTX *ctx = EVP_CIPHER_CTX_new();
    if (ctx == NULL) return LS_ERR_OOM;

    ls_status st = LS_ERR_CRYPTO;
    int len = 0, total = 0;
    if (EVP_DecryptInit_ex(ctx, cipher, NULL, NULL, NULL) != 1) goto done;
    if (EVP_CIPHER_CTX_ctrl(ctx, EVP_CTRL_AEAD_SET_IVLEN, LS_NONCE_LEN, NULL) != 1) goto done;
    if (EVP_DecryptInit_ex(ctx, NULL, NULL, key, nonce) != 1) goto done;
    if (aad_len > 0 && EVP_DecryptUpdate(ctx, NULL, &len, aad, (int)aad_len) != 1) goto done;
    if (ct_len > 0) {
        if (EVP_DecryptUpdate(ctx, pt, &len, ct, (int)ct_len) != 1) goto done;
        total = len;
    }
    if (EVP_CIPHER_CTX_ctrl(ctx, EVP_CTRL_AEAD_SET_TAG, LS_TAG_LEN, (void *)tag) != 1) goto done;
    /* Tag verification happens here; a mismatch fails closed. */
    st = (EVP_DecryptFinal_ex(ctx, pt + total, &len) == 1) ? LS_OK : LS_ERR_AUTH;
done:
    EVP_CIPHER_CTX_free(ctx);
    return st;
}

/* --- container assembly / parsing --- */

static ls_status seal_core(const uint8_t *key, ls_aead aead, uint8_t kdf_id,
                           const uint8_t salt[LS_SALT_LEN],
                           const uint8_t *pt, size_t pt_len,
                           uint8_t **out, size_t *out_len) {
    const EVP_CIPHER *cipher = cipher_for(aead);
    if (cipher == NULL) return LS_ERR_FORMAT;

    size_t blob_len = LS_OVERHEAD + pt_len;
    uint8_t *blob = (uint8_t *)malloc(blob_len);
    if (blob == NULL) return LS_ERR_OOM;

    memcpy(blob + OFF_MAGIC, LS_MAGIC, sizeof LS_MAGIC);
    blob[OFF_VERSION] = LS_VERSION;
    blob[OFF_AEAD]    = (uint8_t)aead;
    blob[OFF_KDF]     = kdf_id;
    blob[7]           = 0; /* reserved */
    memcpy(blob + OFF_SALT, salt, LS_SALT_LEN);
    if (RAND_bytes(blob + OFF_NONCE, LS_NONCE_LEN) != 1) {
        OPENSSL_cleanse(blob, blob_len);
        free(blob);
        return LS_ERR_CRYPTO;
    }

    uint8_t *ct  = blob + LS_HEADER_LEN;
    uint8_t *tag = blob + LS_HEADER_LEN + pt_len;
    ls_status st = aead_encrypt(cipher, key, blob + OFF_NONCE, blob, LS_HEADER_LEN,
                                pt, pt_len, ct, tag);
    if (st != LS_OK) {
        OPENSSL_cleanse(blob, blob_len);
        free(blob);
        return st;
    }
    *out = blob;
    *out_len = blob_len;
    return LS_OK;
}

static ls_status decrypt_blob(const uint8_t *key, const uint8_t *blob, size_t blob_len,
                              uint8_t **out, size_t *out_len) {
    if (blob_len < LS_OVERHEAD) return LS_ERR_FORMAT;
    if (memcmp(blob + OFF_MAGIC, LS_MAGIC, sizeof LS_MAGIC) != 0) return LS_ERR_FORMAT;
    if (blob[OFF_VERSION] != LS_VERSION) return LS_ERR_FORMAT;
    if (blob[OFF_KDF] != LS_KDF_NONE && blob[OFF_KDF] != LS_KDF_ARGON2ID) return LS_ERR_FORMAT;
    const EVP_CIPHER *cipher = cipher_for((ls_aead)blob[OFF_AEAD]);
    if (cipher == NULL) return LS_ERR_FORMAT;

    size_t ct_len = blob_len - LS_OVERHEAD;
    const uint8_t *nonce = blob + OFF_NONCE;
    const uint8_t *ct    = blob + LS_HEADER_LEN;
    const uint8_t *tag   = blob + LS_HEADER_LEN + ct_len;

    size_t alloc = ct_len ? ct_len : 1; /* avoid malloc(0) ambiguity */
    uint8_t *pt = (uint8_t *)malloc(alloc);
    if (pt == NULL) return LS_ERR_OOM;

    ls_status st = aead_decrypt(cipher, key, nonce, blob, LS_HEADER_LEN, ct, ct_len, tag, pt);
    if (st != LS_OK) {
        OPENSSL_cleanse(pt, alloc); /* never release unverified plaintext */
        free(pt);
        return st;
    }
    *out = pt;
    *out_len = ct_len;
    return LS_OK;
}

/* --- public: raw-key API --- */

ls_status ls_seal(const uint8_t key[LS_KEY_LEN], ls_aead aead,
                  const uint8_t *plaintext, size_t pt_len,
                  uint8_t **out, size_t *out_len) {
    if (out == NULL || out_len == NULL) return LS_ERR_NULL_ARG;
    *out = NULL; *out_len = 0;
    if (key == NULL || (plaintext == NULL && pt_len > 0)) return LS_ERR_NULL_ARG;
    if (pt_len > LS_MAX_PLAINTEXT) return LS_ERR_TOO_LARGE;

    uint8_t salt[LS_SALT_LEN];
    memset(salt, 0, sizeof salt); /* unused under kdf_id = none */
    return seal_core(key, aead, LS_KDF_NONE, salt, plaintext, pt_len, out, out_len);
}

ls_status ls_open(const uint8_t key[LS_KEY_LEN],
                  const uint8_t *blob, size_t blob_len,
                  uint8_t **out, size_t *out_len) {
    if (out == NULL || out_len == NULL) return LS_ERR_NULL_ARG;
    *out = NULL; *out_len = 0;
    if (key == NULL || blob == NULL) return LS_ERR_NULL_ARG;
    return decrypt_blob(key, blob, blob_len, out, out_len);
}

/* --- public: passphrase API --- */

ls_status ls_seal_passphrase(const uint8_t *passphrase, size_t pass_len, ls_aead aead,
                             const uint8_t *plaintext, size_t pt_len,
                             uint8_t **out, size_t *out_len) {
    if (out == NULL || out_len == NULL) return LS_ERR_NULL_ARG;
    *out = NULL; *out_len = 0;
    if (passphrase == NULL || (plaintext == NULL && pt_len > 0)) return LS_ERR_NULL_ARG;
    if (pt_len > LS_MAX_PLAINTEXT) return LS_ERR_TOO_LARGE;

    uint8_t salt[LS_SALT_LEN];
    if (RAND_bytes(salt, sizeof salt) != 1) return LS_ERR_CRYPTO;

    uint8_t key[LS_KEY_LEN];
    ls_status st = ls_derive_key(passphrase, pass_len, salt, sizeof salt, key);
    if (st == LS_OK) {
        st = seal_core(key, aead, LS_KDF_ARGON2ID, salt, plaintext, pt_len, out, out_len);
    }
    OPENSSL_cleanse(key, sizeof key);
    OPENSSL_cleanse(salt, sizeof salt);
    return st;
}

ls_status ls_open_passphrase(const uint8_t *passphrase, size_t pass_len,
                             const uint8_t *blob, size_t blob_len,
                             uint8_t **out, size_t *out_len) {
    if (out == NULL || out_len == NULL) return LS_ERR_NULL_ARG;
    *out = NULL; *out_len = 0;
    if (passphrase == NULL || blob == NULL) return LS_ERR_NULL_ARG;
    if (blob_len < LS_OVERHEAD) return LS_ERR_FORMAT;
    if (memcmp(blob + OFF_MAGIC, LS_MAGIC, sizeof LS_MAGIC) != 0) return LS_ERR_FORMAT;
    if (blob[OFF_VERSION] != LS_VERSION) return LS_ERR_FORMAT;
    if (blob[OFF_KDF] != LS_KDF_ARGON2ID) return LS_ERR_FORMAT;

    uint8_t key[LS_KEY_LEN];
    ls_status st = ls_derive_key(passphrase, pass_len, blob + OFF_SALT, LS_SALT_LEN, key);
    if (st == LS_OK) {
        st = decrypt_blob(key, blob, blob_len, out, out_len);
    }
    OPENSSL_cleanse(key, sizeof key);
    return st;
}

void ls_free(uint8_t *buf, size_t len) {
    if (buf == NULL) return;
    if (len > 0) OPENSSL_cleanse(buf, len);
    free(buf);
}
